public class Personagens {
    String nome;
    int vida;
    int forca;
    int defesa;
    int agilidade;

    public Personagens(String nome, int vida, int forca, int defesa, int agilidade) {
        this.nome = nome;
        this.vida = vida;
        this.forca = forca;
        this.defesa = defesa;
        this.agilidade = agilidade;
    }
}
